#!/usr/bin/env python3

"""This concats a string a returns a string
"""


def concat(str1: str, str2: str) -> str:
    """This function concats a string

    Args:
        str1 (str): a string
        str2 (str): a string

    Returns:
        str: returns the concatenated string
    """
    return "{}{}".format(str1, str2)
